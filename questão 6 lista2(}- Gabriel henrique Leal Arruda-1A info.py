x = float(input("digite o primeiro número a ser calculado:"))
x2 = float(input("digite o segundo número a ser calculado:"))
x3 = input("digite o um dos sinais para o cálculo:+,-,* ou /: ")
if x3 == "+":
    print("o resultado é:",x+x2)
if x3 == "-":
    print("o resultado é:",x-x2)
if x3 == "*":
    print("o resultado é:",x*x2)
if x3 == "/":
    print("o resultado é:",x/x2)
